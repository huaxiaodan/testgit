public class RateBean {
    private String date;//日期
    private String dollar;//美元
    private String eru;//欧元
    private String hkDollar;//港币

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDollar() {
        return dollar;
    }

    public void setDollar(String dollar) {
        this.dollar = dollar;
    }

    public String getEru() {
        return eru;
    }

    public void setEru(String eru) {
        this.eru = eru;
    }

    public String getHkDollar() {
        return hkDollar;
    }

    public void setHkDollar(String hkDollar) {
        this.hkDollar = hkDollar;
    }
}
